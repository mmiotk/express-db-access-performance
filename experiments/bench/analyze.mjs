// Post-hoc statistical analysis over results/raw.json (which must carry per-run
// sample arrays; produced by the current runner). Emits three LaTeX tables:
//   results/tables/cv_all.tex             — coefficient of variation, layer × pattern
//   results/tables/significance_deep_fetch.tex — adjacent-layer paired ratio + permutation p
//   results/tables/resources.tex          — per-layer CPU% and peak RSS on the deep fetch
import { readFile, writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import {
  median, cv, cliffsDelta,
  geomMeanRatio, winFraction, pairedPermutation, wilcoxonSignedRank, pairedBootstrapRatioCI,
} from './stats.mjs';

const here = dirname(fileURLToPath(import.meta.url));
const tablesDir = join(here, '..', 'results', 'tables');
const tex = (s) => String(s).replace(/_/g, '\\_');

const ORDER = ['pg', 'mysql2', 'knex', 'drizzle', 'prisma', 'sequelize', 'typeorm', 'objection', 'mikroorm'];
const PATTERNS = ['point_read', 'range_scan', 'deep_fetch', 'aggregation', 'write'];
const ENGINE = process.env.ENGINE || 'postgres';

const rows = JSON.parse(await readFile(join(here, '..', 'results', 'raw.json'), 'utf8'));
if (!rows.some((r) => Array.isArray(r.rps_samples))) {
  console.error('raw.json has no per-run samples — re-run bench/runner.mjs first.');
  process.exit(1);
}
const get = (adapter, endpoint, engine = ENGINE) => rows.find((r) => r.adapter === adapter && r.endpoint === endpoint && r.engine === engine);
const layersFor = (engine) => ORDER.filter((a) => rows.some((r) => r.adapter === a && r.engine === engine));

// --- 1. CV table (layer × pattern), one engine -----------------------------
function cvTable(engine) {
  const layers = layersFor(engine);
  const reps = get(layers[0], 'point_read', engine)?.repeats ?? 'R';
  const head = `Layer & ${PATTERNS.map((p) => `\\textbf{${p.replace('_', '\\_')}}`).join(' & ')} \\\\`;
  const body = layers.map((a) => {
    const cells = PATTERNS.map((p) => {
      const r = get(a, p, engine);
      return r && r.rps_samples ? (cv(r.rps_samples) * 100).toFixed(1) : '--';
    });
    return `${tex(a)} & ${cells.join(' & ')} \\\\`;
  });
  return `% auto-generated by bench/analyze.mjs
\\begin{table}[htbp]
  \\centering
  \\caption{Coefficient of variation (\\%) of throughput across the ${reps} repeated runs, per access layer and pattern on ${engine==="postgres"?"PostgreSQL":"MySQL"}. Low values indicate stable measurements.}
  \\label{tab:cv}
  \\begin{adjustbox}{max width=\\textwidth}
  \\begin{tabular}{l ${PATTERNS.map(() => 'r').join(' ')}}
    \\toprule
    ${head}
    \\midrule
    ${body.join('\n    ')}
    \\bottomrule
  \\end{tabular}
  \\end{adjustbox}
\\end{table}
`;
}

// --- 2. Significance of adjacent layers on the deep fetch ------------------
// The design is paired: within each replicate every layer is driven by the
// identical request stream, so adjacent layers are compared with PAIRED tests on
// the per-replicate log-ratios (permutation + Wilcoxon signed-rank), reporting the
// geometric-mean paired ratio with a paired bootstrap 95% CI and paired dominance.
// Seeded PRNG so every resample and permutation is bit-reproducible.
function mulberry32(a) {
  return function () { a |= 0; a = a + 0x6D2B79F5 | 0; let t = Math.imul(a ^ a >>> 15, 1 | a); t = t + Math.imul(t ^ t >>> 7, 61 | t) ^ t; return ((t ^ t >>> 14) >>> 0) / 4294967296; };
}
const rand = mulberry32(0x5eed);
const PERM_B = 20000;
function bootCI(samples, B = 2000) {
  if (!samples || samples.length < 2) return [NaN, NaN];
  const meds = [];
  for (let b = 0; b < B; b++) {
    const s = samples.map(() => samples[Math.floor(rand() * samples.length)]);
    meds.push(median(s));
  }
  meds.sort((x, y) => x - y);
  return [Math.round(meds[Math.floor(0.025 * B)]), Math.round(meds[Math.floor(0.975 * B)])];
}
const pairedRows = []; // machine-readable paired stats for propagation/cross-check
function significanceTable(engine, endpoint = 'deep_fetch') {
  const ranked = layersFor(engine)
    .map((a) => ({ a, r: get(a, endpoint, engine) }))
    .filter((x) => x.r && x.r.rps_samples)
    .sort((x, y) => y.r.rps - x.r.rps);
  const ci = (r) => { const [lo, hi] = bootCI(r.rps_samples); return `${r.rps}~[${lo}--${hi}]`; };
  const lines = [];
  for (let i = 0; i + 1 < ranked.length; i++) {
    const A = ranked[i]; const B = ranked[i + 1];
    // pair on the common replicate length (all reads are 25/25)
    const n = Math.min(A.r.rps_samples.length, B.r.rps_samples.length);
    const a = A.r.rps_samples.slice(0, n), b = B.r.rps_samples.slice(0, n);
    const ratio = geomMeanRatio(a, b);
    const [rlo, rhi] = pairedBootstrapRatioCI(a, b, { B: 5000, level: 0.95, rand });
    const win = winFraction(a, b);
    const perm = pairedPermutation(a, b, { B: PERM_B, rand });
    const wil = wilcoxonSignedRank(a, b);
    // At the resolution floor the observed statistic beat every permutation, so
    // p = 1/(B+1) exactly (not an upper bound); print that value, else the estimate.
    const pStr = perm.p <= 1 / (PERM_B + 1) ? `$5.0{\\times}10^{-5}$` : perm.p.toFixed(4);
    pairedRows.push({ engine, endpoint, pair: `${A.a}>${B.a}`, ratio: +ratio.toFixed(3),
      ratioCI: [+rlo.toFixed(3), +rhi.toFixed(3)], winFraction: +win.toFixed(2),
      perm_p: perm.p, wilcoxon_p: +wil.p.toFixed(4), cliffs_delta_unpaired: +cliffsDelta(a, b).toFixed(2) });
    lines.push(`${tex(A.a)} $>$ ${tex(B.a)} & ${ci(A.r)} & ${ci(B.r)} & ${ratio.toFixed(2)}~[${rlo.toFixed(2)}--${rhi.toFixed(2)}] & ${win.toFixed(2)} & ${pStr} \\\\`);
  }
  return `% auto-generated by bench/analyze.mjs
\\begin{table}[htbp]
  \\centering
  \\caption{Paired comparison of adjacently ranked access layers on the deep/nested
    fetch (${engine==="postgres"?"PostgreSQL":"MySQL"}), over the ${ranked[0].r.repeats} repeated runs. Because
    every layer runs the identical per-replicate request stream, layers are compared
    on their per-replicate throughput ratios: median throughput (req/s) with
    bootstrap 95\\% CI, the geometric-mean paired ratio A$/$B with a paired bootstrap
    95\\% CI, paired dominance (fraction of replicates in which A exceeds B), and the
    two-sided paired sign-flip permutation $p$ (${PERM_B} permutations; a value
    of $5.0{\\times}10^{-5}$ is the resolution floor $1/(B{+}1)=1/20{,}001$, i.e.
    the observed statistic exceeded every permutation; the Wilcoxon signed-rank
    test agrees, replication package).}
  \\label{tab:significance}
  \\begin{adjustbox}{max width=\\textwidth}
  \\begin{tabular}{l r r r r r}
    \\toprule
    Comparison (A $>$ B) & med.\\ A [95\\% CI] & med.\\ B [95\\% CI] & ratio A$/$B [95\\% CI] & dom. & perm.\\ $p$ \\\\
    \\midrule
    ${lines.join('\n    ')}
    \\bottomrule
  \\end{tabular}
  \\end{adjustbox}
\\end{table}
`;
}

// --- 3. Resource footprint on the deep fetch ------------------------------
function resourceTable(engine, endpoint = 'deep_fetch') {
  const layers = layersFor(engine);
  const body = layers.map((a) => {
    const r = get(a, endpoint, engine);
    return `${tex(a)} & ${tex(r?.category ?? '')} & ${r?.cpu_pct ?? '--'} & ${r?.rss_mb ?? '--'} \\\\`;
  });
  return `% auto-generated by bench/analyze.mjs
\\begin{table}[htbp]
  \\centering
  \\caption{Server-process resource footprint on the deep/nested fetch (${engine==="postgres"?"PostgreSQL":"MySQL"}): median CPU utilization (\\% of one core) and peak resident memory (MB) of the Node.js process under load. The load generator and database run in separate processes.}
  \\label{tab:resources}
  \\begin{tabular}{l l r r}
    \\toprule
    Layer & Category & CPU (\\%) & RSS (MB) \\\\
    \\midrule
    ${body.join('\n    ')}
    \\bottomrule
  \\end{tabular}
\\end{table}
`;
}

// --- 4. Compute efficiency: application-tier vs combined (app + database) CPU --
// The application-only figure (req per app-CPU-second) understates the total
// compute of layers that push more work to the database server; combining app and
// database CPU per completed request gives an end-to-end view (review 6.4). CPU is
// median % of one logical core; the load generator's CPU is excluded (it is only an
// observer-capacity check). Combined cores = (app% + db%)/100.
function cpuEfficiencyTable(engine, endpoint = 'deep_fetch') {
  const layers = layersFor(engine);
  const body = layers.map((a) => {
    const r = get(a, endpoint, engine);
    if (!r) return `${tex(a)} & -- & -- & -- & -- & -- \\\\`;
    const combinedCores = (r.cpu_pct + (r.db_cpu_pct ?? 0)) / 100;
    const appEff = Math.round(r.rps / (r.cpu_pct / 100));
    const combEff = Math.round(r.rps / combinedCores);
    return `${tex(a)} & ${r.cpu_pct} & ${r.db_cpu_pct ?? '--'} & ${combinedCores.toFixed(2)} & ${appEff} & ${combEff} \\\\`;
  });
  return `% auto-generated by bench/analyze.mjs
\\begin{table}[htbp]
  \\centering
  \\caption{Application-tier versus end-to-end CPU efficiency on the deep/nested
    fetch (${engine==="postgres"?"PostgreSQL":"MySQL"}): median CPU (\\% of one logical core) of the Node.js
    process tree and of the database server, the combined core-count, and
    throughput per application-CPU-second and per combined (app$+$database)
    CPU-second. The application-only figure understates the compute of layers that
    delegate more work to the database; the combined figure is the end-to-end view.
    Load-generator CPU is excluded.}
  \\label{tab:cpu_efficiency}
  \\begin{adjustbox}{max width=\\textwidth}
  \\begin{tabular}{l r r r r r}
    \\toprule
    Layer & app CPU\\% & db CPU\\% & comb.\\ cores & req/app-CPU-s & req/comb-CPU-s \\\\
    \\midrule
    ${body.join('\n    ')}
    \\bottomrule
  \\end{tabular}
  \\end{adjustbox}
\\end{table}
`;
}

// --- 4b. Compact main-text resource table (review 6.8): app+db CPU, combined
// efficiency, and peak RSS on the deep fetch, so the headline "five times the CPU"
// and the end-to-end reframe are in the body rather than only the supplement.
function resourceMainTable(engine, endpoint = 'deep_fetch') {
  const layers = layersFor(engine);
  const body = layers.map((a) => {
    const r = get(a, endpoint, engine);
    if (!r) return `${tex(a)} & -- & -- & -- & -- \\\\`;
    const combEff = Math.round(r.rps / ((r.cpu_pct + (r.db_cpu_pct ?? 0)) / 100));
    return `${tex(a)} & ${r.cpu_pct} & ${r.db_cpu_pct ?? '--'} & ${combEff} & ${r.rss_mb ?? '--'} \\\\`;
  });
  return `% auto-generated by bench/analyze.mjs
\\begin{table}[htbp]
  \\centering
  \\caption{Resource footprint on the deep/nested fetch (${engine==="postgres"?"PostgreSQL":"MySQL"}, medians over 25
    replicates): application-process-tree CPU and database-server CPU (\\% of one
    logical core), end-to-end throughput per combined (application$+$database)
    CPU-second, and peak resident memory. Prisma draws about five times the
    application CPU of the other layers at the fixed ten-connection pool; counting
    the database side as well narrows its end-to-end efficiency gap to the native
    driver to about $1.4\\times$. An equal-CPU control (Supplement Table~S4)
    confirms the trade operationally: on a one-core application budget \\texttt{pg}
    reaches 3{,}663~req/s to Prisma's 896. Per-layer efficiency and equal-CPU detail
    are in Supplement Tables~S5 and~S4.}
  \\label{tab:resources_main}
  \\begin{tabular}{l r r r r}
    \\toprule
    Layer & app CPU\\% & db CPU\\% & req/comb-CPU-s & RSS (MB) \\\\
    \\midrule
    ${body.join('\n    ')}
    \\bottomrule
  \\end{tabular}
\\end{table}
`;
}

await writeFile(join(tablesDir, 'cv_all.tex'), cvTable(ENGINE));
await writeFile(join(tablesDir, 'significance_deep_fetch.tex'), significanceTable(ENGINE));
await writeFile(join(tablesDir, 'resources.tex'), resourceTable(ENGINE));
await writeFile(join(tablesDir, 'resources_main.tex'), resourceMainTable(ENGINE));
await writeFile(join(tablesDir, 'cpu_efficiency.tex'), cpuEfficiencyTable(ENGINE));
await writeFile(join(here, '..', 'results', `significance_paired_${ENGINE}.json`), JSON.stringify(pairedRows, null, 2));

// console summary
const maxCv = Math.max(...rows.filter((r) => r.rps_samples).map((r) => cv(r.rps_samples) * 100));
console.log(`[analyze] engine=${ENGINE}: wrote cv_all.tex, significance_deep_fetch.tex, resources.tex, cpu_efficiency.tex`);
console.log(`[analyze] max CV across all cells: ${maxCv.toFixed(1)}%`);
for (const p of pairedRows) console.log(`  paired ${p.pair}: ratio ${p.ratio} [${p.ratioCI}] dom ${p.winFraction} perm_p ${p.perm_p.toExponential(1)} wilcoxon_p ${p.wilcoxon_p}`);
