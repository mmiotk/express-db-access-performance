// Post-hoc statistical analysis over results/raw.json (which must carry per-run
// sample arrays; produced by the current runner). Emits three LaTeX tables:
//   results/tables/cv_all.tex             — coefficient of variation, layer × pattern
//   results/tables/significance_deep_fetch.tex — adjacent-layer Cliff's delta + MWU p
//   results/tables/resources.tex          — per-layer CPU% and peak RSS on the deep fetch
import { readFile, writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { median, cv, mannWhitneyU, cliffsDelta, cliffsMagnitude } from './stats.mjs';

const here = dirname(fileURLToPath(import.meta.url));
const tablesDir = join(here, '..', 'results', 'tables');
const tex = (s) => String(s).replace(/_/g, '\\_');

const ORDER = ['pg', 'mysql2', 'knex', 'drizzle', 'prisma', 'sequelize', 'typeorm', 'objection', 'mikroorm'];
const PATTERNS = ['point_read', 'range_scan', 'deep_fetch', 'aggregation', 'write'];
const ENGINE = process.env.ENGINE || 'postgres';

const rows = JSON.parse(await readFile(join(here, '..', 'results', 'raw.json'), 'utf8'));
if (!rows.some((r) => Array.isArray(r.rps_samples))) {
  console.error('raw.json has no per-run samples — re-run bench/runner.mjs first.');
  process.exit(1);
}
const get = (adapter, endpoint, engine = ENGINE) => rows.find((r) => r.adapter === adapter && r.endpoint === endpoint && r.engine === engine);
const layersFor = (engine) => ORDER.filter((a) => rows.some((r) => r.adapter === a && r.engine === engine));

// --- 1. CV table (layer × pattern), one engine -----------------------------
function cvTable(engine) {
  const layers = layersFor(engine);
  const reps = get(layers[0], 'point_read', engine)?.repeats ?? 'R';
  const head = `Layer & ${PATTERNS.map((p) => `\\textbf{${p.replace('_', '\\_')}}`).join(' & ')} \\\\`;
  const body = layers.map((a) => {
    const cells = PATTERNS.map((p) => {
      const r = get(a, p, engine);
      return r && r.rps_samples ? (cv(r.rps_samples) * 100).toFixed(1) : '--';
    });
    return `${tex(a)} & ${cells.join(' & ')} \\\\`;
  });
  return `% auto-generated by bench/analyze.mjs
\\begin{table}[htbp]
  \\centering
  \\caption{Coefficient of variation (\\%) of throughput across the ${reps} measured runs, per access layer and pattern on ${engine}. Low values indicate stable measurements.}
  \\label{tab:cv}
  \\begin{adjustbox}{max width=\\textwidth}
  \\begin{tabular}{l ${PATTERNS.map(() => 'r').join(' ')}}
    \\toprule
    ${head}
    \\midrule
    ${body.join('\n    ')}
    \\bottomrule
  \\end{tabular}
  \\end{adjustbox}
\\end{table}
`;
}

// --- 2. Significance of adjacent layers on the deep fetch ------------------
function significanceTable(engine, endpoint = 'deep_fetch') {
  const ranked = layersFor(engine)
    .map((a) => ({ a, r: get(a, endpoint, engine) }))
    .filter((x) => x.r && x.r.rps_samples)
    .sort((x, y) => y.r.rps - x.r.rps);
  const lines = [];
  for (let i = 0; i + 1 < ranked.length; i++) {
    const A = ranked[i]; const B = ranked[i + 1];
    const d = cliffsDelta(A.r.rps_samples, B.r.rps_samples);
    const { p } = mannWhitneyU(A.r.rps_samples, B.r.rps_samples);
    const pStr = p < 0.001 ? '$<$0.001' : p.toFixed(3);
    lines.push(`${tex(A.a)} $>$ ${tex(B.a)} & ${A.r.rps} & ${B.r.rps} & ${d.toFixed(2)} (${cliffsMagnitude(d)}) & ${pStr} \\\\`);
  }
  return `% auto-generated by bench/analyze.mjs
\\begin{table}[htbp]
  \\centering
  \\caption{Pairwise comparison of adjacently ranked access layers on the deep/nested fetch (${engine}), by median throughput (req/s), Cliff's~$\\delta$ effect size, and Mann--Whitney~$U$ two-sided $p$.}
  \\label{tab:significance}
  \\begin{adjustbox}{max width=\\textwidth}
  \\begin{tabular}{l r r l r}
    \\toprule
    Comparison (A $>$ B) & med.\\ A & med.\\ B & Cliff's $\\delta$ & MWU $p$ \\\\
    \\midrule
    ${lines.join('\n    ')}
    \\bottomrule
  \\end{tabular}
  \\end{adjustbox}
\\end{table}
`;
}

// --- 3. Resource footprint on the deep fetch ------------------------------
function resourceTable(engine, endpoint = 'deep_fetch') {
  const layers = layersFor(engine);
  const body = layers.map((a) => {
    const r = get(a, endpoint, engine);
    return `${tex(a)} & ${tex(r?.category ?? '')} & ${r?.cpu_pct ?? '--'} & ${r?.rss_mb ?? '--'} \\\\`;
  });
  return `% auto-generated by bench/analyze.mjs
\\begin{table}[htbp]
  \\centering
  \\caption{Server-process resource footprint on the deep/nested fetch (${engine}): median CPU utilization (\\% of one core) and peak resident memory (MB) of the Node.js process under load. The load generator and database run in separate processes.}
  \\label{tab:resources}
  \\begin{tabular}{l l r r}
    \\toprule
    Layer & Category & CPU (\\%) & RSS (MB) \\\\
    \\midrule
    ${body.join('\n    ')}
    \\bottomrule
  \\end{tabular}
\\end{table}
`;
}

await writeFile(join(tablesDir, 'cv_all.tex'), cvTable(ENGINE));
await writeFile(join(tablesDir, 'significance_deep_fetch.tex'), significanceTable(ENGINE));
await writeFile(join(tablesDir, 'resources.tex'), resourceTable(ENGINE));

// console summary
const maxCv = Math.max(...rows.filter((r) => r.rps_samples).map((r) => cv(r.rps_samples) * 100));
console.log(`[analyze] engine=${ENGINE}: wrote cv_all.tex, significance_deep_fetch.tex, resources.tex`);
console.log(`[analyze] max CV across all cells: ${maxCv.toFixed(1)}%`);
