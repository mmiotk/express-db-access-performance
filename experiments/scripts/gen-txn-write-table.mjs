// Deterministically rebuild Supplement Table S8 from the archived measurement.
import { readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const results = join(here, '..', 'results');
const data = JSON.parse(await readFile(join(results, 'txn-write.json'), 'utf8'));
const rows = data.map((row) =>
  `    \\texttt{${row.adapter}} & ${row.rps} & ${row.p99} \\\\`
).join('\n');

const tex = `% auto-generated by scripts/gen-txn-write-table.mjs from results/txn-write.json
\\begin{table}[htbp]
  \\centering
  \\caption{Transactional write throughput (req/s) and tail latency (p99, ms) for
    \\texttt{POST /threads}, which inserts a post and five comments in a single
    transaction, on PostgreSQL under default durability (50 connections, median of
    five runs). A representative layer of each tier is shown; the full matrix is left
    to future work. Between-layer spread is $3.7\\times$ (\\texttt{pg} 2718 down to
    Prisma 733, now the slowest), well below the $7.15\\times$ read spread, and the
    ordering does not reproduce the single-row-insert ordering because Prisma moves
    from mid-pack to last. This exploratory five-layer PostgreSQL subset does not
    support a general claim about writes.}
  \\label{tab:txn_write}
  \\begin{tabular}{l r r}
    \\toprule
    Layer & req/s & p99 (ms) \\\\
    \\midrule
${rows}
    \\bottomrule
  \\end{tabular}
\\end{table}
`;

await writeFile(join(results, 'tables', 'txn_write.tex'), tex);
await writeFile(join(here, '..', '..', 'paper', 'tables', 'txn_write.tex'), tex);
console.log('wrote txn_write.tex from archived results/txn-write.json');
