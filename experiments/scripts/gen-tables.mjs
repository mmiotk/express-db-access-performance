// Regenerate the derived LaTeX tables that are not produced by the runner or
// analyze.mjs directly: the default-vs-relaxed durability table, the equal-CPU
// control table, and the CPU/throughput trade-off figure. Reads the campaign JSON
// under results/ and writes into results/tables/. Deterministic, no measurement.
//   node scripts/gen-tables.mjs
import { readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const R = (p) => JSON.parse(readFileSyncish(p));
import { readFileSync } from 'node:fs';
function readFileSyncish(p) { return readFileSync(join(here, '..', 'results', p), 'utf8'); }
const tablesDir = join(here, '..', 'results', 'tables');
const med = (a) => { const s = [...a].sort((x, y) => x - y); const m = Math.floor(s.length / 2); return s.length % 2 ? s[m] : Math.round((s[m - 1] + s[m]) / 2); };

const raw = R('raw.json');
const rel = R('raw-writes-relaxed.json');
const equalcpu = R('equalcpu.json');
const ORDER = ['pg', 'mysql2', 'pg-tuned', 'mysql2-tuned', 'knex', 'drizzle', 'prisma', 'sequelize', 'typeorm', 'objection', 'mikroorm'];
const g = (rows, a, e, ep) => rows.find((r) => r.adapter === a && r.engine === e && (!ep || r.endpoint === ep));

// --- durability: default (raw.json) vs relaxed (raw-writes-relaxed.json) ------
{
  const cell = (a, e) => {
    const d = g(raw, a, e, 'write'), r = g(rel, a, e);
    if (!d || !r) return '-- & -- & --';
    const dm = med(d.rps_samples), rm = med(r.rps_samples);
    return `${dm} & ${rm} & ${(rm / dm).toFixed(2)}`;
  };
  const rows = ORDER.filter((a) => g(raw, a, 'postgres', 'write') || g(raw, a, 'mysql', 'write'))
    .map((a) => `    \\texttt{${a}} & ${cell(a, 'postgres')} & ${cell(a, 'mysql')} \\\\`).join('\n');
  const tex = `% auto-generated by scripts/gen-tables.mjs — write throughput default vs relaxed durability
\\begin{table}[htbp]
  \\centering
  \\caption{Insert throughput (req/s) under the default durability configuration
    (PostgreSQL \\texttt{fsync=on}, \\texttt{synchronous\\_commit=on}; MySQL
    \\texttt{innodb\\_flush\\_log\\_at\\_trx\\_commit=1}, \\texttt{sync\\_binlog=1};
    median of 25 replicates) and under the relaxed regime (all of the above off;
    median of 10), with the relaxed$\\div$default ratio. At 50 concurrent
    connections, group commit amortizes the per-commit flushes: relaxing
    durability buys at most 14\\% on PostgreSQL (and $\\le$4\\% on the
    layer-bound ORMs) and 19--24\\% on MySQL.}
  \\label{tab:durability}
  \\begin{adjustbox}{max width=\\textwidth}
  \\begin{tabular}{l r r r r r r}
    \\toprule
    & \\multicolumn{3}{c}{PostgreSQL} & \\multicolumn{3}{c}{MySQL} \\\\
    \\cmidrule(lr){2-4}\\cmidrule(lr){5-7}
    Layer & default & relaxed & ratio & default & relaxed & ratio \\\\
    \\midrule
${rows}
    \\bottomrule
  \\end{tabular}
  \\end{adjustbox}
\\end{table}
`;
  await writeFile(join(tablesDir, 'durability.tex'), tex);
}

// --- equal-CPU control (equalcpu.json) ----------------------------------------
{
  const layers = ['pg', 'prisma', 'mikroorm'];
  const cores = [...new Set(equalcpu.map((r) => parseInt(r.cores)))].sort((a, b) => a - b);
  const line = (a) => cores.map((c) => {
    const r = equalcpu.find((x) => x.adapter === a && parseInt(x.cores) === c);
    return r ? String(med(r.rps_samples)) : '--';
  }).join(' & ');
  const body = layers.map((a) => `    \\texttt{${a}} & ${line(a)} \\\\`).join('\n');
  const tex = `% auto-generated by scripts/gen-tables.mjs — equal-CPU-slice control
\\begin{table}[htbp]
  \\centering
  \\caption{Deep-fetch throughput (req/s, PostgreSQL, median of 3) with the
    server process confined by \\texttt{taskset} to 1, 2, or 4 cores (database
    and load generator pinned to disjoint cores). \\texttt{pg} reaches full
    throughput on a single core; Prisma requires roughly four to approach it,
    quantifying the CPU subsidy behind its near-native throughput.}
  \\label{tab:equalcpu}
  \\begin{tabular}{l r r r}
    \\toprule
    Layer & 1 core & 2 cores & 4 cores \\\\
    \\midrule
${body}
    \\bottomrule
  \\end{tabular}
\\end{table}
`;
  await writeFile(join(tablesDir, 'equalcpu.tex'), tex);
}

// --- CPU/throughput trade-off figure (raw.json, deep/PG) ----------------------
{
  const LAYERS = ['pg', 'pg-tuned', 'knex', 'drizzle', 'prisma', 'sequelize', 'typeorm', 'objection', 'mikroorm'];
  const pts = LAYERS.map((a) => {
    const r = g(raw, a, 'postgres', 'deep_fetch');
    return `(${r.cpu_pct},${med(r.rps_samples)}) [${a}]`;
  }).join(' ');
  let tex = await readFile(join(tablesDir, 'fig_cpu_tradeoff.tex'), 'utf8');
  tex = tex.replace(/coordinates \{[^}]*\};/, `coordinates {${pts}};`);
  await writeFile(join(tablesDir, 'fig_cpu_tradeoff.tex'), tex);
}

console.log('gen-tables: wrote durability.tex, equalcpu.tex, fig_cpu_tradeoff.tex');
