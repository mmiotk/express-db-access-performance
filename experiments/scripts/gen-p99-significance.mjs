// p99 counterpart of bench/analyze.mjs significanceTable (reviewer: p99 is a primary
// outcome and must carry the same paired inference as throughput). Ranks the idiomatic
// layers by median p99 (lower = better tail) on the deep fetch and, for each adjacent
// pair, reports the paired p99 effect size + tests on the per-replicate p99 ratios,
// both engines. All from results/raw.json's p99_samples; no re-run. Seed 0x5eed and
// PERM_B=20000 match the throughput significance table for bit-consistency.
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { median, geomMeanRatio, pairedBootstrapRatioCI, winFraction, pairedPermutation, wilcoxonSignedRank } from '../bench/stats.mjs';

const here = dirname(fileURLToPath(import.meta.url));
const resultsDir = join(here, '..', 'results');
const raw = JSON.parse(readFileSync(join(resultsDir, 'raw.json'), 'utf8'));

function mulberry32(a) { return function () { a |= 0; a = a + 0x6D2B79F5 | 0; let t = Math.imul(a ^ a >>> 15, 1 | a); t = t + Math.imul(t ^ t >>> 7, 61 | t) ^ t; return ((t ^ t >>> 14) >>> 0) / 4294967296; }; }
function fnv1a(str) { let h = 0x811c9dc5; for (let i = 0; i < str.length; i++) { h ^= str.charCodeAt(i); h = Math.imul(h, 0x01000193); } return h >>> 0; }
const rand = mulberry32(0x5eed);          // stream for the paired tests (ratio CI, permutation)
const PERM_B = 20000;
// Per-cell deterministically-seeded p99 CI (same formula/seed/B as ci-tables.mjs), so a
// layer's p99 CI is identical whether it appears as A or B and matches the pattern tables.
function bootCI(samples, key, B = 5000) {
  if (!samples || samples.length < 2) return [NaN, NaN];
  const r = mulberry32(fnv1a(key));
  const meds = [];
  for (let b = 0; b < B; b++) { const s = samples.map(() => samples[Math.floor(r() * samples.length)]); meds.push(median(s)); }
  meds.sort((x, y) => x - y);
  return [Math.round(meds[Math.floor(0.025 * B)]), Math.round(meds[Math.floor(0.975 * B)])];
}

const IDIOM = ['pg', 'mysql2', 'knex', 'drizzle', 'prisma', 'sequelize', 'typeorm', 'objection', 'mikroorm'];
const tx = (a) => `\\texttt{${a}}`;
const get = (a, eng) => raw.find((r) => r.adapter === a && r.endpoint === 'deep_fetch' && r.engine === eng);

function block(engine) {
  const ranked = IDIOM.map((a) => ({ a, r: get(a, engine) })).filter((x) => x.r && x.r.p99_samples).sort((x, y) => x.r.p99 - y.r.p99);
  const cim = (r) => { const [lo, hi] = bootCI(r.p99_samples, `${r.adapter}|${engine}|deep_fetch|p99`); return `${r.p99}~[${lo}--${hi}]`; };
  const lines = [];
  for (let i = 0; i + 1 < ranked.length; i++) {
    const A = ranked[i], B = ranked[i + 1]; // A = lower (better) p99, B = higher (worse) p99
    const n = Math.min(A.r.p99_samples.length, B.r.p99_samples.length);
    const a = A.r.p99_samples.slice(0, n), b = B.r.p99_samples.slice(0, n);
    const ratio = geomMeanRatio(b, a);                                  // B/A > 1: slower tail over faster
    const [rlo, rhi] = pairedBootstrapRatioCI(b, a, { B: 5000, level: 0.95, rand });
    const win = winFraction(b, a);                                      // fraction of replicates B's p99 > A's
    const perm = pairedPermutation(b, a, { B: PERM_B, rand });
    const pStr = perm.p <= 1 / (PERM_B + 1) ? '$5.0{\\times}10^{-5}$' : perm.p.toFixed(4);
    lines.push(`${tx(A.a)} $<$ ${tx(B.a)} & ${cim(A.r)} & ${cim(B.r)} & ${ratio.toFixed(2)}~[${rlo.toFixed(2)}--${rhi.toFixed(2)}] & ${win.toFixed(2)} & ${pStr}`);
  }
  return lines;
}

