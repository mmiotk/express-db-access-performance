const body = `    \\multicolumn{6}{l}{\\emph{PostgreSQL}} \\\\\n    ${pg.join(' \\\\\n    ')} \\\\\n    \\midrule\n    \\multicolumn{6}{l}{\\emph{MySQL}} \\\\\n    ${my.join(' \\\\\n    ')} \\\\`;

