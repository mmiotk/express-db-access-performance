// Generates the co-primary deep-fetch regime table (review 6.4) from
// results/deepfetch-regimes.json. Two regimes per ORM x engine: documentation-primary (the
// strategy fixed by the adapter-selection rule) and performance-conscious (the faster of the two
// documented loading strategies, admitted only where byte-identical). The native drivers expose a
// single hand-authored join (no documented alternative) and appear as a same-harness reference row
// (records tagged role:'native-baseline', appended by scripts/deepfetch-native.mjs). Writes
// results/tables/deepfetch_regimes.tex and paper/tables/deepfetch_regimes.tex.
import { readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const all = JSON.parse(await readFile(join(here, '..', 'results', 'deepfetch-regimes.json'), 'utf8'));
const native = all.filter((r) => r.role === 'native-baseline');
const data = all.filter((r) => r.role !== 'native-baseline');
const ORDER = ['sequelize', 'mikroorm', 'objection', 'typeorm'];
const SWITCH = { sequelize: 'join$\\to$select-in', typeorm: 'join$\\to$select-in', mikroorm: 'join$\\to$select-in', objection: 'select-in$\\to$join' };
const rec = (eng, a) => data.find((x) => x.engine === eng && x.adapter === a && x.identical);
const nat = (eng) => native.find((x) => x.engine === eng);

// native reference row: single hand-authored join, one regime (no documented alternative)
const natRow = native.length
  ? `    native (\\texttt{pg}/\\texttt{mysql2}) & hand-join (single) & ${nat('postgres') ? nat('postgres').doc_primary_rps : '--'} & --- & ${nat('mysql') ? nat('mysql').doc_primary_rps : '--'} & --- \\\\\n    \\midrule\n`
  : '';

const rows = ORDER.map((a) => {
  const pg = rec('postgres', a), my = rec('mysql', a);
  const c = (r, key) => (r ? r[key] : '--');
  return `    \\texttt{${a}} & ${SWITCH[a]} & ${c(pg, 'doc_primary_rps')} & ${c(pg, 'perf_conscious_rps')} & ${c(my, 'doc_primary_rps')} & ${c(my, 'perf_conscious_rps')} \\\\`;
}).join('\n');

const excluded = data.filter((x) => !x.identical).map((x) => `${x.adapter}/${x.engine} (${x.reason})`);
const exclNote = excluded.length
  ? ` A dash marks a layer$\\times$engine cell whose documented alternative is not a byte-identical drop-in in the pinned versions (${excluded.join('; ')}); there the performance-conscious regime coincides with documentation-primary by definition.`
  : '';

const tex = `% auto-generated by scripts/gen-deepfetch-table.mjs
\\begin{table}[htbp]
  \\centering
  \\caption{The two co-primary deep-fetch regimes, measured on one harness (fixed post id,
    a warmup pass then 25 timed repeats each; absolute req/s therefore differ from the seeded-id
    primary matrix by design, but every cell here shares the harness, including the native
    reference row). \\emph{doc-primary} is each layer's documentation-selected loading strategy;
    \\emph{perf-consc.} is the performance-conscious regime --- the faster of the layer's two
    documented deep-fetch strategies (join versus select-in), admitted only where byte-identical to
    the documentation-primary output. For Sequelize and MikroORM the documentation-selected join is
    already the faster strategy, so the two regimes coincide; only Objection on MySQL gains (its
    documented single-join alternative beats its select-in default), and even then its
    performance-conscious throughput stays below the native driver.${exclNote}}
  \\label{tab:deepfetch_regimes}
  \\begin{tabular}{l l r r r r}
    \\toprule
    & & \\multicolumn{2}{c}{PostgreSQL (req/s)} & \\multicolumn{2}{c}{MySQL (req/s)} \\\\
    \\cmidrule(lr){3-4}\\cmidrule(lr){5-6}
    Layer & Documented alt. & doc-primary & perf-consc. & doc-primary & perf-consc. \\\\
    \\midrule
${natRow}${rows}
    \\bottomrule
  \\end{tabular}
\\end{table}
`;
await writeFile(join(here, '..', 'results', 'tables', 'deepfetch_regimes.tex'), tex);
await writeFile(join(here, '..', '..', 'paper', 'tables', 'deepfetch_regimes.tex'), tex);

console.log('ORM cells (identical alt):');
for (const x of data.filter((r) => r.identical)) console.log(`  ${x.adapter}/${x.engine}: doc=${x.doc_primary_rps} alt=${x.alt_rps} perf=${x.perf_conscious_rps} (x${x.perf_over_doc})`);
console.log('native: ' + (native.map((x) => `${x.adapter}/${x.engine}=${x.doc_primary_rps}`).join(', ') || 'none'));
console.log('excluded: ' + (excluded.join('; ') || 'none'));
const gains = data.filter((x) => x.identical && x.perf_conscious_rps > x.doc_primary_rps);
console.log('perf-conscious > doc-primary: ' + (gains.map((x) => `${x.adapter}/${x.engine} +${((x.perf_conscious_rps / x.doc_primary_rps - 1) * 100).toFixed(1)}% (perf=${x.perf_conscious_rps})`).join(', ') || 'none'));
if (native.length && gains.length) for (const g of gains) { const n = nat(g.engine); if (n) console.log(`  ${g.adapter}/${g.engine} perf ${g.perf_conscious_rps} vs native ${n.doc_primary_rps} -> ${(g.perf_conscious_rps / n.doc_primary_rps).toFixed(2)}x native (${g.perf_conscious_rps < n.doc_primary_rps ? 'below' : 'ABOVE'} native)`); }
console.log('wrote results/tables/deepfetch_regimes.tex + paper/tables/deepfetch_regimes.tex');
