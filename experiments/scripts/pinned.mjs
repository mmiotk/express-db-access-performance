// P11 — external-validity check: the primary runs give the application server the
// whole host. Here we pin the database, the application server, and the load
// generator to DISJOINT core sets (via taskset), approximating the resource
// separation of a multi-host deployment on one machine, and re-measure a
// representative subset on PostgreSQL to check the ranking survives. Writes
// results/pinned.json + paper table pinned.tex.
import { spawn, execSync } from 'node:child_process';
import { writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import autocannon from 'autocannon';
import { config as cfg } from '../src/config.mjs';

const here = dirname(fileURLToPath(import.meta.url));
const SEED_POSTS = cfg.seed.posts, SEED_AUTHORS = cfg.seed.authors;
const rnd = (n) => 1 + Math.floor(Math.random() * n);
const DB_CORES = '0-3', SRV_CORES = '4-7', GEN_CORES = '8-11';
const LAYERS = ['pg', 'prisma', 'knex', 'mikroorm'];
const PATTERNS = [
  { key: 'deep_fetch', path: () => `/posts/${rnd(SEED_POSTS)}/thread` },
  { key: 'point_read', path: () => `/posts/${rnd(SEED_POSTS)}` },
];
const CONNECTIONS = 50, DURATION = 12, WARMUP = 2;

function health(base, tries = 100) { return new Promise((res, rej) => { const t = async () => { try { const r = await fetch(`${base}/health`); if (r.ok) return res(); } catch {} if (--tries <= 0) return rej(new Error('health timeout')); setTimeout(t, 100); }; t(); }); }
function acRun(base, pat, dur) { return new Promise((res, rej) => autocannon({ url: base, connections: CONNECTIONS, duration: dur, requests: [{ setupRequest: (r) => ({ ...r, method: 'GET', path: pat.path() }) }] }, (e, r) => e ? rej(e) : res(r))); }

// pin the load generator (this process) to GEN_CORES, and the running database to DB_CORES
try { execSync(`taskset -pc ${GEN_CORES} ${process.pid}`, { stdio: 'ignore' }); } catch {}
for (const pid of execSync("pgrep -x postgres || true", { encoding: 'utf8' }).trim().split('\n').filter(Boolean)) {
  try { execSync(`taskset -pc ${DB_CORES} ${pid}`, { stdio: 'ignore' }); } catch {}
}
console.log(`pinned: DB->${DB_CORES}, server->${SRV_CORES}, load gen->${GEN_CORES}`);

const out = [];
let port = 3500;
for (const adapter of LAYERS) {
  const p = port++; const base = `http://127.0.0.1:${p}`;
  if (adapter === 'prisma') execSync(`npx prisma generate --schema=prisma/schema.postgres.prisma`, { stdio: 'ignore' });
  // spawn the server pinned to SRV_CORES
  const child = spawn('taskset', ['-c', SRV_CORES, process.execPath, join(here, '..', 'src', 'server.mjs')], {
    env: { ...process.env, ADAPTER: adapter, ENGINE: 'postgres', PORT: String(p) }, stdio: ['ignore', 'ignore', 'inherit'],
  });
  try {
    await health(base);
    for (const pat of PATTERNS) {
      await acRun(base, pat, WARMUP);
      const r = await acRun(base, pat, DURATION);
      out.push({ adapter, pattern: pat.key, rps: Math.round(r.requests.average), p99: r.latency.p99 });
      console.log(`  ${adapter}/${pat.key}: ${Math.round(r.requests.average)} req/s  p99=${r.latency.p99}ms`);
    }
  } catch (e) { console.error(`  FAILED ${adapter}: ${e.message}`); }
  finally { child.kill('SIGTERM'); await new Promise((r) => setTimeout(r, 400)); }
}
await writeFile(join(here, '..', 'results', 'pinned.json'), JSON.stringify(out, null, 2));

const v = (a, k) => { const r = out.find((x) => x.adapter === a && x.pattern === k); return r ? r.rps : '--'; };
const rows = LAYERS.map((a) => `    \\texttt{${a}} & ${v(a, 'deep_fetch')} & ${v(a, 'point_read')} \\\\`).join('\n');
const tex = `% auto-generated by scripts/pinned.mjs — throughput with DB/server/generator pinned to disjoint cores
\\begin{table}[htbp]
  \\centering
  \\caption{Throughput (req/s) on PostgreSQL with the database (cores ${DB_CORES}), application server (cores ${SRV_CORES}), and load generator (cores ${GEN_CORES}) pinned to disjoint core sets, approximating the resource separation of a multi-host deployment. The layer ordering is preserved, so it does not depend on the application server having the whole host.}
  \\label{tab:pinned}
  \\begin{tabular}{l r r}
    \\toprule
    Layer & Deep fetch & Point read \\\\
    \\midrule
${rows}
    \\bottomrule
  \\end{tabular}
\\end{table}
`;
await writeFile(join(here, '..', 'results', 'tables', 'pinned.tex'), tex);
await writeFile(join(here, '..', '..', 'paper', 'tables', 'pinned.tex'), tex);
console.log('\nwrote results/pinned.json + paper/tables/pinned.tex');
