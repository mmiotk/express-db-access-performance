// Per-cell uncertainty for the primary tables (review 4, §6.8/E7). Regenerates the
// five per-pattern throughput/p99 tables from results/raw.json with a 95% bootstrap
// confidence interval on the throughput of EVERY layer x engine cell, so the primary
// outcomes are no longer point estimates. Percentile bootstrap resampling replicate
// indices, seeded (matches the paired analysis). Overwrites tables/<pattern>.tex.
import { readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { median } from '../bench/stats.mjs';

const here = dirname(fileURLToPath(import.meta.url));
const raw = JSON.parse(await readFile(join(here, '..', 'results', 'raw.json'), 'utf8'));
const ORDER = ['pg', 'mysql2', 'pg-tuned', 'mysql2-tuned', 'knex', 'drizzle', 'prisma', 'sequelize', 'typeorm', 'objection', 'mikroorm'];
const PATTERNS = { point_read: 'Point read', range_scan: 'Keyset range scan', deep_fetch: 'Deep/nested fetch', aggregation: 'Aggregation', write: 'Insert' };
const CAT = { pg: 'native-driver', mysql2: 'native-driver', 'pg-tuned': 'native-tuned', 'mysql2-tuned': 'native-tuned', knex: 'query-builder', drizzle: 'orm', prisma: 'orm', sequelize: 'orm', typeorm: 'orm', objection: 'orm', mikroorm: 'orm' };

function mulberry32(a) { return function () { a |= 0; a = a + 0x6D2B79F5 | 0; let t = Math.imul(a ^ a >>> 15, 1 | a); t = t + Math.imul(t ^ t >>> 7, 61 | t) ^ t; return ((t ^ t >>> 14) >>> 0) / 4294967296; }; }
function fnv1a(str) { let h = 0x811c9dc5; for (let i = 0; i < str.length; i++) { h ^= str.charCodeAt(i); h = Math.imul(h, 0x01000193); } return h >>> 0; }
const rand = mulberry32(0x57a75);
function ci(samples, B = 5000) {
  if (!samples || samples.length < 2) return null;
  const s = [];
  for (let b = 0; b < B; b++) { const rs = samples.map(() => samples[Math.floor(rand() * samples.length)]); s.push(median(rs)); }
  s.sort((x, y) => x - y);
  return [Math.round(s[Math.floor(0.025 * B)]), Math.round(s[Math.floor(0.975 * B)])];
}
// Per-cell deterministically-seeded percentile bootstrap of the median, used for the
// p99 CIs so the value is independent of iteration order and identical across every
// generator (pattern tables, Table S30, analysis2.json) that reports it.
function ciSeeded(samples, key, B = 5000) {
  if (!samples || samples.length < 2) return null;
  const r = mulberry32(fnv1a(key));
  const s = [];
  for (let b = 0; b < B; b++) { const rs = samples.map(() => samples[Math.floor(r() * samples.length)]); s.push(median(rs)); }
  s.sort((x, y) => x - y);
  return [Math.round(s[Math.floor(0.025 * B)]), Math.round(s[Math.floor(0.975 * B)])];
}
const get = (a, ep, eng) => raw.find((r) => r.adapter === a && r.endpoint === ep && r.engine === eng);
const cell = (a, ep, eng) => {
  const r = get(a, ep, eng);
  if (!r) return { rps: '--', p99: '--' };
  const c = ci(r.rps_samples);
  const c99 = ciSeeded(r.p99_samples, `${a}|${eng}|${ep}|p99`);
  return {
    rps: c ? `${r.rps}~[${c[0]}--${c[1]}]` : String(r.rps),
    p99: c99 ? `${r.p99}~[${c99[0]}--${c99[1]}]` : String(r.p99),
  };
};

for (const [ep, label] of Object.entries(PATTERNS)) {
  const rows = ORDER.filter((a) => get(a, ep, 'postgres') || get(a, ep, 'mysql')).map((a) => {
    const pg = cell(a, ep, 'postgres'), my = cell(a, ep, 'mysql');
    return `    ${a} & ${CAT[a]} & ${pg.rps} & ${pg.p99} & ${my.rps} & ${my.p99} \\\\`;
  }).join('\n');
  const tex = `% auto-generated by scripts/ci-tables.mjs — throughput with 95% bootstrap CI
\\begin{table}[htbp]
  \\centering
  \\caption{${label}: throughput (req/s, higher is better; median with a
    within-campaign 95\\% bootstrap interval over the 25 repeated runs --- run-to-run
    variability within one host and campaign, not across hardware or days) and
    response-time p99 (ms, lower is better; median run-level p99 with the same
    within-campaign 95\\% bootstrap interval) by access layer and engine.}
  \\label{tab:${ep}}
  \\begin{adjustbox}{max width=\\textwidth}
  \\begin{tabular}{l l r r r r}
    \\toprule
    & & \\multicolumn{2}{c}{PostgreSQL} & \\multicolumn{2}{c}{MySQL} \\\\
    \\cmidrule(lr){3-4}\\cmidrule(lr){5-6}
    Layer & Category & req/s [95\\% CI] & p99 [95\\% CI] & req/s [95\\% CI] & p99 [95\\% CI] \\\\
    \\midrule
${rows}
    \\bottomrule
  \\end{tabular}
  \\end{adjustbox}
\\end{table}
`;
  await writeFile(join(here, '..', 'results', 'tables', `${ep}.tex`), tex);
}
console.log('wrote CI-augmented tables: ' + Object.keys(PATTERNS).join('.tex, ') + '.tex');
