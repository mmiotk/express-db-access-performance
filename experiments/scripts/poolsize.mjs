// Pool-size sensitivity (revision round 2, review 6.8). The primary campaign holds
// every layer's connection pool at 10 to isolate the access layer from pool tuning;
// this sweep varies the pool for a representative layer of each tier on the deep
// fetch (PostgreSQL, 50 connections) to show how sensitive the ranking is to that
// one configuration choice. Off-measurement; writes results/poolsize.json and
// results/tables/poolsize.tex.
import { spawn, execSync } from 'node:child_process';
import { readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import autocannon from 'autocannon';
import { config as cfg } from '../src/config.mjs';

const here = dirname(fileURLToPath(import.meta.url));
const SEED_POSTS = cfg.seed.posts;
const rnd = (n) => 1 + Math.floor(Math.random() * n);
const CONNECTIONS = 50, DURATION = 10, WARMUP = 5;
const LAYERS = (process.env.PS_LAYERS ?? 'pg,knex,prisma,mikroorm').split(',');
const POOLS = (process.env.PS_POOLS ?? '1,2,5,10,20,50').split(',').map(Number);
const REPS = Number(process.env.PS_REPS ?? 3);

function health(base, tries = 100) { return new Promise((res, rej) => { const t = async () => { try { const r = await fetch(`${base}/health`); if (r.ok) return res(); } catch {} if (--tries <= 0) return rej(new Error('health timeout')); setTimeout(t, 100); }; t(); }); }
function run(base, dur) {
  return new Promise((res, rej) => autocannon({
    url: base, connections: CONNECTIONS, duration: dur,
    requests: [{ setupRequest: (r) => ({ ...r, method: 'GET', path: `/posts/${rnd(SEED_POSTS)}/thread` }) }],
  }, (e, r) => e ? rej(e) : res(r)));
}
const med = (a) => [...a].sort((x, y) => x - y)[Math.floor(a.length / 2)];

const out = [];
let port = 3900;
for (const adapter of LAYERS) {
  if (adapter === 'prisma') execSync('npx prisma generate --schema=prisma/schema.postgres.prisma', { stdio: 'ignore' });
  for (const pool of POOLS) {
    const p = port++; const base = `http://127.0.0.1:${p}`;
    const child = spawn(process.execPath, [join(here, '..', 'src', 'server.mjs')], {
      env: { ...process.env, TZ: 'UTC', ADAPTER: adapter, ENGINE: 'postgres', PORT: String(p), POOL_MIN: String(pool), POOL_MAX: String(pool) },
      stdio: ['ignore', 'ignore', 'inherit'],
    });
    try {
      await health(base);
      await run(base, WARMUP);
      const samples = [];
      for (let i = 0; i < REPS; i++) samples.push(Math.round((await run(base, DURATION)).requests.average));
      const rps = med(samples);
      out.push({ adapter, pool, rps, rps_samples: samples });
      console.log(`  ${adapter} pool=${pool}: ${rps} req/s`);
    } catch (e) { console.error(`  FAILED ${adapter} pool=${pool}: ${e.message}`); }
    finally { child.kill('SIGTERM'); await new Promise((r) => setTimeout(r, 400)); }
  }
}
await writeFile(join(here, '..', 'results', 'poolsize.json'), JSON.stringify(out, null, 2));

// table: rows = layers, cols = pool sizes
const rows = LAYERS.map((a) => {
  const cells = POOLS.map((pl) => { const r = out.find((x) => x.adapter === a && x.pool === pl); return r ? String(r.rps) : '--'; });
  return `    \\texttt{${a}} & ${cells.join(' & ')} \\\\`;
}).join('\n');
const tex = `% auto-generated by scripts/poolsize.mjs — pool-size sensitivity
\\begin{table}[htbp]
  \\centering
  \\caption{Deep-fetch throughput (req/s, PostgreSQL, 50 connections, median of
    ${REPS}) as the connection pool varies from 1 to 50 for a representative layer
    of each tier. The primary campaign fixes the pool at 10; the three-band
    ordering (native and query-engine ORM on top, the query builder in the middle,
    the data-mapper ORM below) is preserved at every pool size, and each layer
    saturates at a pool well below 50, so the fixed-pool choice does not drive the
    ranking.}
  \\label{tab:poolsize}
  \\begin{tabular}{l ${POOLS.map(() => 'r').join(' ')}}
    \\toprule
    Layer & ${POOLS.map((p) => `${p}`).join(' & ')} \\\\
    \\midrule
${rows}
    \\bottomrule
  \\end{tabular}
\\end{table}
`;
await writeFile(join(here, '..', 'results', 'tables', 'poolsize.tex'), tex);
console.log('wrote results/poolsize.json + results/tables/poolsize.tex');
