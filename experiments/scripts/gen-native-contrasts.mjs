// Predefined contrasts against the native-driver reference on the deep fetch
// (reviewer point 8.1). Unlike the adjacent-pair tests, the reference (the native
// driver) and the set of contrasts (each portable layer vs the reference) are fixed
// in advance, so they are NOT conditional on the observed ranking. For each portable
// layer we report: the geometric-mean paired ratio native/layer (how many times
// faster the native driver is, per replicate), a within-campaign 95% paired-bootstrap
// interval, and the paired dominance (fraction of the 25 replicates in which the
// native driver is faster). Regenerates from results/raw.json (no re-run).
//
// Writes ONLY tables/native_contrasts.tex (does not touch any hand-tuned table).
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { geomMeanRatio, pairedBootstrapRatioCI, winFraction } from '../bench/stats.mjs';

const here = dirname(fileURLToPath(import.meta.url));
const resultsDir = join(here, '..', 'results');
const rows = JSON.parse(readFileSync(join(resultsDir, 'raw.json'), 'utf8'));

// Seeded PRNG, per-cell key, so the bootstrap is order-independent and reproducible.
function mulberry32(a) { return function () { a |= 0; a = a + 0x6D2B79F5 | 0; let t = Math.imul(a ^ a >>> 15, 1 | a); t = t + Math.imul(t ^ t >>> 7, 61 | t) ^ t; return ((t ^ t >>> 14) >>> 0) / 4294967296; }; }
function keyseed(s) { let h = 0x811c9dc5; for (let i = 0; i < s.length; i++) { h ^= s.charCodeAt(i); h = Math.imul(h, 0x01000193); } return h >>> 0; }

const PORT = ['knex', 'drizzle', 'prisma', 'sequelize', 'typeorm', 'objection', 'mikroorm'];
const NATIVE = { postgres: 'pg', mysql: 'mysql2' };
const samp = (a, e) => { const r = rows.find((x) => x.adapter === a && x.engine === e && x.endpoint === 'deep_fetch'); return r ? r.rps_samples : null; };

const cell = (layer, eng) => {
  const nat = samp(NATIVE[eng], eng), lay = samp(layer, eng);
  if (!nat || !lay) return { ratio: '--', dom: '--' };
  const ratio = geomMeanRatio(nat, lay); // native / layer
  const [lo, hi] = pairedBootstrapRatioCI(nat, lay, { B: 5000, level: 0.95, rand: mulberry32(keyseed(`${layer}|${eng}`)) });
  const dom = winFraction(nat, lay);
  return { ratio: `${ratio.toFixed(2)}~[${lo.toFixed(2)},${hi.toFixed(2)}]`, dom: dom.toFixed(2) };
};

const body = PORT.map((a) => {
  const pg = cell(a, 'postgres'), my = cell(a, 'mysql');
  return `    \\texttt{${a}} & ${pg.ratio} & ${pg.dom} & ${my.ratio} & ${my.dom} \\\\`;
}).join('\n');

const tex = `% auto-generated by scripts/gen-native-contrasts.mjs from results/raw.json
\\begin{table}[htbp]
  \\centering
  \\small
  \\caption{Predefined contrasts against the native-driver reference on the deep fetch, fixed in
    advance rather than selected after the ranking (reviewer 8.1). For each portable layer: the
    geometric-mean paired ratio of \\emph{native-driver to layer} throughput (how many times faster the
    native driver is, per replicate), a within-campaign 95\\% paired-bootstrap interval, and the paired
    \\emph{dominance} (fraction of the 25 replicates in which the native driver is faster). Because the
    reference and the contrast set are predeclared, these are not conditional on the observed ordering,
    unlike the adjacent-pair tests. Values are within-campaign, not deployment-general; the reference is
    \\texttt{pg} on PostgreSQL and \\texttt{mysql2} on MySQL.}
  \\label{tab:native_contrasts}
  \\begin{tabular}{l r c r c}
    \\toprule
    & \\multicolumn{2}{c}{PostgreSQL} & \\multicolumn{2}{c}{MySQL} \\\\
    \\cmidrule(lr){2-3}\\cmidrule(lr){4-5}
    Layer & native$/$layer [95\\% CI] & dom. & native$/$layer [95\\% CI] & dom. \\\\
    \\midrule
${body}
    \\bottomrule
  \\end{tabular}
\\end{table}
`;
writeFileSync(join(resultsDir, 'tables', 'native_contrasts.tex'), tex);
writeFileSync(join(here, '..', '..', 'paper', 'tables', 'native_contrasts.tex'), tex);
console.log('wrote native_contrasts.tex');
