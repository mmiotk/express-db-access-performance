// Generate the main-text tail-regime contrast table (tab:tail_regimes) that puts
// the two p99 readings side by side per layer and engine: the high-load p99 under
// equal closed-loop demand (the 50-connection deep-fetch cell of the primary matrix,
// results/raw.json) versus the coordinated-omission-corrected p99 at matched 50%
// utilization (results/utilization.{engine}.json). The point of the table is the
// contrast: the wide high-load ladder collapses to a narrow band once each layer is
// driven at the same fraction of its own capacity, so the high-load gap is a
// capacity/queueing effect rather than a sub-saturation latency difference. Regenerates from the
// same raw data as tab:deep_fetch (identical .p99 field) and Supplement Tables S21/S22.
import { readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const R = (f) => readFile(join(here, '..', 'results', f), 'utf8').then(JSON.parse);
const out = async (name, tex) => {
  await writeFile(join(here, '..', 'results', 'tables', name), tex);
  await writeFile(join(here, '..', '..', 'paper', 'tables', name), tex);
};

const raw = await R('raw.json');
const uPg = await R('utilization.postgres.json');
const uMy = await R('utilization.mysql.json');

const ORDER = ['pg', 'mysql2', 'knex', 'drizzle', 'prisma', 'sequelize', 'typeorm', 'objection', 'mikroorm'];
const CAT = {
  pg: 'native-driver', mysql2: 'native-driver', knex: 'query-builder',
  drizzle: 'orm', prisma: 'orm', sequelize: 'orm', typeorm: 'orm', objection: 'orm', mikroorm: 'orm',
};

// High-load p99 (equal closed-loop demand): the deep-fetch cell of the primary matrix.
const demand = (a, eng) => {
  const r = raw.find((x) => x.adapter === a && x.endpoint === 'deep_fetch' && x.engine === eng);
  return r ? String(r.p99) : '--';
};
// Matched 50% utilization p99 (coordinated-omission-corrected open-loop).
const matched = (a, arr) => {
  const r = arr.find((x) => x.adapter === a && x.fraction === 0.5);
  return r ? String(r.p99_med) : '--';
};

const rows = ORDER.map((a) =>
  `    \\texttt{${a}} & ${CAT[a]} & ${demand(a, 'postgres')} & ${matched(a, uPg)} & ${demand(a, 'mysql')} & ${matched(a, uMy)} \\\\`
).join('\n');

const tex = `% auto-generated by scripts/gen-tail-regimes.mjs from results/raw.json + utilization.{engine}.json
\\begin{table}[htbp]
  \\centering
  \\caption{The deep-fetch tail read two ways, per layer and engine. \\emph{Equal demand}
    is the high-load response-time p99 (ms) at the fixed 50-connection closed-loop point --- the
    headline deep-fetch p99, which at this saturating load largely tracks
    capacity and connection-acquisition queueing. \\emph{50\\% util.} is the
    coordinated-omission-corrected p99 when each layer is instead offered half of \\emph{its own}
    saturating throughput (Supplement Tables~S21--S22). The wide equal-demand ladder
    (\\texttt{pg}~20 to \\texttt{mikroorm}~116~ms on PostgreSQL) collapses to a $2$--$5$~ms band at
    matched utilization, so the high-load gap is a capacity-and-queueing effect, \\emph{not} a
    sub-saturation latency difference; the matched-utilization reading is the more informative one.}
  \\label{tab:tail_regimes}
  \\begin{tabular}{l l r r r r}
    \\toprule
    & & \\multicolumn{2}{c}{PostgreSQL p99 (ms)} & \\multicolumn{2}{c}{MySQL p99 (ms)} \\\\
    \\cmidrule(lr){3-4}\\cmidrule(lr){5-6}
    Layer & Category & equal demand & 50\\% util. & equal demand & 50\\% util. \\\\
    \\midrule
${rows}
    \\bottomrule
  \\end{tabular}
\\end{table}
`;

await out('tail_regimes.tex', tex);
console.log('wrote tail_regimes.tex');
