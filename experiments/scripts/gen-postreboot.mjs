// Emit the post-restart robustness supplement table from results/postreboot.json.
import { readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
const here = dirname(fileURLToPath(import.meta.url));
const d = JSON.parse(await readFile(join(here, '..', 'results', 'postreboot.json'), 'utf8'));
const eng = (e) => (e === 'mysql' ? 'MySQL' : 'PostgreSQL');
const rows = d.map((r) => `    ${eng(r.engine)} & \\texttt{${r.adapter}} & ${r.primary_deep_rps} & ${r.deep_rps} & ${r.ratio.toFixed(2)} \\\\`).join('\n');
const tex = `% auto-generated by scripts/gen-postreboot.mjs
\\begin{table}[htbp]
  \\centering
  \\caption{Environmental-robustness check (review 6.7): as the campaign's final
    stage, after a full restart of both database engines (cold buffer pools, fresh
    connections), deep-fetch throughput of a representative subset was re-measured
    against the primary median. The layer ranking reproduces on both engines;
    absolute throughput drifts with host conditions (larger on PostgreSQL), a
    cold-buffer and run-to-run variation the paper's \\emph{relative} analysis is
    designed to absorb. Single host, so this checks restart robustness, not
    cross-machine generalization.}
  \\label{tab:postreboot}
  \\begin{tabular}{l l r r r}
    \\toprule
    Engine & Layer & primary req/s & post-restart req/s & ratio \\\\
    \\midrule
${rows}
    \\bottomrule
  \\end{tabular}
\\end{table}
`;
await writeFile(join(here, '..', 'results', 'tables', 'postreboot.tex'), tex);
await writeFile(join(here, '..', '..', 'paper', 'tables', 'postreboot.tex'), tex);
console.log('wrote postreboot.tex');
