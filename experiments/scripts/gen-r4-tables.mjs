// Generate the R4 experiment tables (utilization, cluster, mixed) from their JSON.
import { readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
const here = dirname(fileURLToPath(import.meta.url));
const R = (f) => readFile(join(here, '..', 'results', f), 'utf8').then(JSON.parse).catch(() => null);
const out2 = async (name, tex) => { await writeFile(join(here, '..', 'results', 'tables', name), tex); await writeFile(join(here, '..', '..', 'paper', 'tables', name), tex); };

// --- utilization: p99 at each utilization fraction, per layer, per engine -----
for (const engine of ['postgres', 'mysql']) {
  const d = await R(`utilization.${engine}.json`); if (!d) continue;
  const layers = [...new Set(d.map((x) => x.adapter))];
  const fr = [...new Set(d.map((x) => x.fraction))].sort((a, b) => a - b);
  const rows = layers.map((a) => {
    const cells = fr.map((f) => { const r = d.find((x) => x.adapter === a && x.fraction === f); return r ? String(r.p99_med) : '--'; });
    return `    \\texttt{${a}} & ${cells.join(' & ')} \\\\`;
  }).join('\n');
  const eng = engine === 'mysql' ? 'MySQL' : 'PostgreSQL';
  const label = engine === 'postgres' ? 'utilization' : 'utilization_mysql';
  const tex = `% auto-generated by scripts/gen-r4-tables.mjs
\\begin{table}[htbp]
  \\centering
  \\caption{Utilization-controlled open-loop tail on the deep fetch (${eng}):
    coordinated-omission-corrected p99 (ms, median of five runs) when each layer is
    offered a fixed fraction of \\emph{its own} saturating throughput. At matched
    utilization the tails are small and similar across layers---the large saturated
    p99 gap is a capacity/queueing effect, not intrinsic tail latency; the tail rises
    only as each layer approaches its own capacity.}
  \\label{tab:${label}}
  \\begin{tabular}{l ${fr.map(() => 'r').join(' ')}}
    \\toprule
    Layer & ${fr.map((f) => `${Math.round(f * 100)}\\%`).join(' & ')} \\\\
    \\midrule
${rows}
    \\bottomrule
  \\end{tabular}
\\end{table}
`;
  await out2(`${label}.tex`, tex);
}

// --- cluster: rps at 1/2/4 workers, per layer, per engine ---------------------
{
  const d = await R('cluster.json');
  if (d) {
    const workers = [...new Set(d.map((x) => x.workers))].sort((a, b) => a - b);
    const rows = [];
    for (const engine of ['postgres', 'mysql']) {
      const eng = engine === 'mysql' ? 'MySQL' : 'PostgreSQL';
      const layers = [...new Set(d.filter((x) => x.engine === engine).map((x) => x.adapter))];
      rows.push(`    \\multicolumn{${workers.length + 1}}{l}{\\emph{${eng}}} \\\\`);
      for (const a of layers) {
        const cells = workers.map((w) => { const r = d.find((x) => x.engine === engine && x.adapter === a && x.workers === w); return r ? String(r.rps_med) : '--'; });
        rows.push(`    \\quad\\texttt{${a}} & ${cells.join(' & ')} \\\\`);
      }
    }
    const tex = `% auto-generated by scripts/gen-r4-tables.mjs
\\begin{table}[htbp]
  \\centering
  \\caption{Multi-worker deep-fetch throughput (req/s, median of three runs) as each
    layer is scaled to 1, 2, and 4 Express workers (node cluster, shared port). Every
    layer is single-core-bound per process, so throughput scales roughly linearly
    with workers until the host saturates: Prisma, low at one worker (PostgreSQL
    1{,}117), rises to 2{,}275 at two and 4{,}449 at four workers, recovering
    native-like aggregate throughput at four times the processes. A layer's low
    per-process throughput is therefore recoverable by horizontal scaling, at a
    proportional cost in cores.}
  \\label{tab:cluster}
  \\begin{tabular}{l ${workers.map(() => 'r').join(' ')}}
    \\toprule
    Layer & ${workers.map((w) => `${w}~worker${w > 1 ? 's' : ''}`).join(' & ')} \\\\
    \\midrule
${rows.join('\n')}
    \\bottomrule
  \\end{tabular}
\\end{table}
`;
    await out2('cluster.tex', tex);
  }
}

// --- mixed read/write ---------------------------------------------------------
{
  const d = await R('mixed.json');
  if (d) {
    const rows = d.map((r) => `    ${r.engine === 'mysql' ? 'MySQL' : 'PG'} & \\texttt{${r.adapter}} & ${r.mix} & ${r.rps_med} & ${r.p99_med} \\\\`).join('\n');
    const tex = `% auto-generated by scripts/gen-r4-tables.mjs
\\begin{table}[htbp]
  \\centering
  \\caption{Mixed read/write workload: throughput (req/s) and p99 (ms) under an
    autocannon request mix interleaving the deep-fetch read with single-row inserts,
    at 90:10 and 70:30 read:write, both engines, median of five runs with a physical
    write reset per run. The layer ordering matches the read ordering and is
    insensitive to the mix (the deep-fetch read dominates); exploratory.}
  \\label{tab:mixed}
  \\begin{tabular}{l l l r r}
    \\toprule
    Engine & Layer & read:write & req/s & p99 \\\\
    \\midrule
${rows}
    \\bottomrule
  \\end{tabular}
\\end{table}
`;
    await out2('mixed.tex', tex);
  }
}
console.log('wrote utilization(.mysql).tex, cluster.tex, mixed.tex');
