// Generates the per-pattern capacity/utilization table (review 6.5) from
// results/scaling_patterns.json. For each (pattern, engine, layer): capacity = max req/s over
// the connection ladder; utilization@50 = (req/s at 50 connections) / capacity; knee = the
// smallest connection count reaching >=95% of that layer's capacity. Reports, per (pattern,
// engine), the median knee and the median and minimum utilization@50 across layers, so the
// reviewer's "unknown relative utilization" at the fixed 50-connection point becomes a measured
// quantity. Writes results/tables/scaling_patterns.tex and paper/tables/scaling_patterns.tex.
import { readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { median } from '../bench/stats.mjs';

const here = dirname(fileURLToPath(import.meta.url));
const data = JSON.parse(await readFile(join(here, '..', 'results', 'scaling_patterns.json'), 'utf8'));
const PATTERNS = [
  ['point_read', 'Point read'], ['range_scan', 'Range scan'], ['deep_fetch', 'Deep fetch'],
  ['aggregation', 'Aggregation'], ['write', 'Insert'],
];
const ENGINES = ['postgres', 'mysql'];
const layersOf = (pat, eng) => [...new Set(data.filter((r) => r.pattern === pat && r.engine === eng).map((r) => r.adapter))];

// per (pattern, engine, adapter): capacity, util@50, knee
function cellStats(pat, eng, a) {
  const pts = data.filter((r) => r.pattern === pat && r.engine === eng && r.adapter === a).sort((x, y) => x.connections - y.connections);
  if (!pts.length) return null;
  const cap = Math.max(...pts.map((p) => p.rps));
  const at50 = pts.find((p) => p.connections === 50);
  const util50 = at50 ? at50.rps / cap : null;
  const knee = pts.find((p) => p.rps >= 0.95 * cap);
  return { cap, util50, knee: knee ? knee.connections : null };
}

function agg(pat, eng) {
  const stats = layersOf(pat, eng).map((a) => cellStats(pat, eng, a)).filter(Boolean);
  const utils = stats.map((s) => s.util50).filter((x) => x != null);
  const knees = stats.map((s) => s.knee).filter((x) => x != null);
  return {
    n: stats.length,
    utilMed: utils.length ? median(utils) : null,
    utilMin: utils.length ? Math.min(...utils) : null,
    kneeMed: knees.length ? median(knees) : null,
  };
}

const pct = (x) => (x == null ? '--' : `${Math.round(x * 100)}\\%`);
const rows = PATTERNS.map(([key, label]) => {
  const pg = agg(key, 'postgres'), my = agg(key, 'mysql');
  return `    ${label} & ${pg.kneeMed ?? '--'} & ${pct(pg.utilMed)} (${pct(pg.utilMin)}) & ${my.kneeMed ?? '--'} & ${pct(my.utilMed)} (${pct(my.utilMin)}) \\\\`;
}).join('\n');

const tex = `% auto-generated by scripts/gen-scaling-patterns-table.mjs
\\begin{table}[htbp]
  \\centering
  \\caption{Per-pattern capacity and utilization at the fixed 50-connection operating point, from a
    concurrency sweep (connection ladder 1--200) of every access layer on all five patterns and both
    engines. \\emph{Knee} is the median (across layers) smallest connection count reaching 95\\% of a
    layer's own saturating throughput; \\emph{util.\\ @50} is the median (and, in parentheses,
    minimum) across layers of the ratio (throughput at 50 connections)$/$(saturating throughput). The
    knee sits at or below 50 connections for every pattern on both engines, and the median utilization
    at 50 connections is 97--100\% (minimum across layers 82\%), so the fixed 50-connection point
    places all five patterns at high utilization of their own capacity. The capacity-identification
    stage of the protocol is thus demonstrated for all five patterns, not only the deep fetch; the
    equal-utilization and equal-compute conditions remain demonstrated on the deep fetch.}
  \\label{tab:scaling_patterns}
  \\begin{tabular}{l r r r r}
    \\toprule
    & \\multicolumn{2}{c}{PostgreSQL} & \\multicolumn{2}{c}{MySQL} \\\\
    \\cmidrule(lr){2-3}\\cmidrule(lr){4-5}
    Pattern & knee & util.\\ @50 (min) & knee & util.\\ @50 (min) \\\\
    \\midrule
${rows}
    \\bottomrule
  \\end{tabular}
\\end{table}
`;
await writeFile(join(here, '..', 'results', 'tables', 'scaling_patterns.tex'), tex);
await writeFile(join(here, '..', '..', 'paper', 'tables', 'scaling_patterns.tex'), tex);

// console summary for the prose
console.log('per-pattern (engine): median knee | util@50 median (min) | n layers');
for (const [key, label] of PATTERNS) for (const eng of ENGINES) {
  const a = agg(key, eng);
  console.log(`  ${label}/${eng}: knee=${a.kneeMed} util50=${a.utilMed != null ? (a.utilMed * 100).toFixed(0) + '%' : '--'} (min ${a.utilMin != null ? (a.utilMin * 100).toFixed(0) + '%' : '--'}) n=${a.n}`);
}
// any pattern where 50 is materially below capacity (util < 90%)?
const below = [];
for (const [key, label] of PATTERNS) for (const eng of ENGINES) { const a = agg(key, eng); if (a.utilMin != null && a.utilMin < 0.90) below.push(`${label}/${eng} min ${(a.utilMin * 100).toFixed(0)}%`); }
console.log('cells with min util@50 < 90%: ' + (below.join('; ') || 'none'));
console.log('wrote results/tables/scaling_patterns.tex + paper/tables/scaling_patterns.tex');
