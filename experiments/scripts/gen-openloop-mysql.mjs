// Emit the MySQL open-loop supplement table from results/openloop2.mysql.json
// (openloop2.mjs writes the PostgreSQL table inline; this is its MySQL companion,
// review 6.2 — the open-loop intrinsic-tail check on both engines).
import { readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const data = JSON.parse(await readFile(join(here, '..', 'results', 'openloop2.mysql.json'), 'utf8'));
const layers = [...new Set(data.map((r) => r.adapter))];
const rates = [...new Set(data.map((r) => r.offered))].sort((a, b) => a - b);
const cell = (a, rate) => { const r = data.find((x) => x.adapter === a && x.offered === rate); return r ? `${Math.round(r.p99)}${r.saturated ? '$^\\dagger$' : ''}` : '--'; };
const rows = layers.map((a) => `    \\texttt{${a}} & ${rates.map((rate) => cell(a, rate)).join(' & ')} \\\\`).join('\n');
const tex = `% auto-generated by scripts/gen-openloop-mysql.mjs
\\begin{table}[htbp]
  \\centering
  \\caption{Open-loop tail latency on \\textbf{MySQL}, corrected for coordinated
    omission: p99 (ms) on the deep/nested fetch under a constant offered request
    rate, every latency measured from the intended start time and timed-out requests
    clipped into the distribution. $^\\dagger$marks cells that did not sustain the
    offered rate. As on PostgreSQL (Supplement Table~S6), the sub-saturation tail is
    flat and small and each layer collapses once the offered rate crosses its
    capacity, so the intrinsic-tail ordering is engine-robust.}
  \\label{tab:openloop_mysql}
  \\begin{tabular}{l ${rates.map(() => 'r').join(' ')}}
    \\toprule
    Layer & ${rates.map((r) => r + '/s').join(' & ')} \\\\
    \\midrule
${rows}
    \\bottomrule
  \\end{tabular}
\\end{table}
`;
await writeFile(join(here, '..', 'results', 'tables', 'openloop_mysql.tex'), tex);
await writeFile(join(here, '..', '..', 'paper', 'tables', 'openloop_mysql.tex'), tex);
console.log('wrote openloop_mysql.tex');
